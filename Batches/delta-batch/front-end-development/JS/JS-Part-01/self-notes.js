// JavaScript Basics Programming Language:

// 1. Variable – is simply the name of a storage location. e.g.- age(var) = 23(value).
// 2. Data Types – detects the automatic data type of variable
//     a. Primitives Types –
//         i. Number – 
//             1. Positive & Negative
//             2. Integers
//             3. Floating Numbers – with decimals
//         ii.	Boolean
//         iii. String
//         iv.	Undefined
//         v. Null
//         vi.	Bigint
//         vii. Symbol
//     Note: typeof var-name
// 3. Operations – 
//     i. Modulo (remainder operator) ¬– e.g.- 12%5 = 2
//     ii.	Exponentiation (power operator) – e.g.- 2**3 = 8
// 4. NaN in JS – global property is a value representing Not-A-Number
//     i. 0/0
//     ii.	NaN+1
//     iii. NaN*1
//     iv.	NaN + NaN
// 5. Operator Precedence – is the general order of solving an expression
//     i. ()
//     ii.	**
//     iii. *,/,%
//     iv.	+,-  
//         1. e.g.- (5+2)/7+1*2                     
// 6. let Keyword – Syntax of declaring variables
//     i. let age = 23;
//     ii.	let cgpa;
// 7. const Keyword – value of constants can’t be changed with re-assignment & they can’t be assigned again.
//     i. const pie = 3.14;
// 8. var Keyword – old syntax of writing variables
//     i. var age = 23;
//     ii.	var cgpa = 8.1;
// 9. Identifier Rules – all JavaScript variables must be identified with unique names (identifiers).
//     i.	Names can contain letters, digits, underscores, and dollar signs. (no space).
//     ii.	Names must begin with a letter.
//     iii. Names can also begin with $ and _.
//     iv.	Names are case sensitive (y and Y are different variables).
//     v. Reserved words (like JavaScript keywords) CANNOT be used as names.
//     vi.	Way of writing identifiers.
//         1. camelCase
//         2. snake_case
//         3. PascalCase
// 10. TypeScript – Static(fixed) typed, where JS is dynamic(changeable) typed
// 11.	String in JS – are text or sequence of characters.
// 12.	Undefined and Null in JS – 
//     i. undefined – A variable that has not been assigned a value of type undefined.
//     ii.	null – The null value represents the intentional absence of any object value. To be explicitly assigned.
